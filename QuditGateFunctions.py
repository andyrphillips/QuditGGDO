#QFT Gate
class QuditQFT(cirq.SingleQubitGate):
	def _qid_shape_(self):
		return(3,)
	
	def _unitary_(self):
		return (1/np.sqrt(3)) * np.array([[1, 1, 1],
									  [1, w, w**2],
									  [1, w**2, w]])
	def _circuit_diagram_info_(self, args):
		return '[QFT]'
		
