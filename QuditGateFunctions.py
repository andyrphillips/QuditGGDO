#SHIFT gate
class QuditX(cirq.SingleQubitGate):
	def _qid_shape_(self):
		return(3,)
	
	def _unitary_(self):
		return np.array([[0, 0, 1],
						 [1, 0, 0],
						 [0, 1, 0]])
	
	def _circuit_diagram_info_(self, args):
		return '[X]'

